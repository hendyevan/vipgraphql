
import UIKit

// swiftlint:disable discouraged_object_literal

// Custom color using color object literal
// adding to UIColor using extension,
// so it can use anywhere UIColor called.
extension UIColor {
  static let grayTintColor = #colorLiteral(red: 0.5843137255, green: 0.5843137255, blue: 0.5843137255, alpha: 1)
  static let blueTintColor = #colorLiteral(red: 0, green: 0.4784313725, blue: 1, alpha: 1)
  static let background = #colorLiteral(red: 0.9764705882, green: 0.9764705882, blue: 0.9764705882, alpha: 1)
  static let greenAlien = #colorLiteral(red: 0.8274509804, green: 1, blue: 0.1294117647, alpha: 1)
  static let grayHuman = #colorLiteral(red: 0.9294117647, green: 0.9294117647, blue: 0.9294117647, alpha: 1)
}
